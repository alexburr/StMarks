      <p class="lead">Ever wondered about the history of the Coptic Church? We would like to take this opportunity to serve as a resource to help you gain a better understanding of where the Coptic Orthodox Church started, our rich history, and believes. </p>


<p>Read up, this is a beautiful story!</p>

<p>God's promise to His people is always fulfilled. He foretold that He would ride on a light and upon a swift cloud and come to Egypt (Isa 19:1); and in that day there will be an altar to the Lord in the midst of the land of Egypt, and a pillar to the Lord at its border (Isa 19:19). This promise was fulfilled by the flight of the Holy Family from the face of the tyrant Herod to find refuge among the Gentiles. Thus our Lord Jesus Christ came during His childhood to Egypt to lay by Himself the foundation of His Church in Egypt which has become one of the four primary "sees" in the world, among the churches of Jerusalem, Antioch and Rome, and joined later by the "See" of Constantinople.</p>

<p>The star of the Egyptian Church shone through the School of Alexandria which taught Christendom the allegoric and spiritual methods in interpreting the Holy Scripture and was the leader in defending the Orthodox faith on an ecumenical level.</p>

<p>The Christian monastic movement in all its forms started in Egypt, attracting the heart of the Church towards the desert, to practice the angelic inner life. This happened at the time when the doors of the royal court had been opened to the clergy, and this consequently endangered the church, as the quiet and spiritual church work was mixed with the temporal authority and politics of the royal court.</p>

<p>The Egyptian Church carried our Lord Jesus Christ's cross throughout generations, bearing sufferings even from the side of Christians themselves. She continued to offer a countless number of martyrs and confessors throughout ages. Sometimes the&nbsp;towns people were martyred and many struggled to win the crowns of martyrdom happily and with a joyful heart.</p>

<p>Our Church is both ancient and new. Ancient in&nbsp;that it is&nbsp;apostolic, founded by St. Mark the Evangelist traditionally practicing the apostolic faith without deviation.&nbsp;The church&nbsp;is new through&nbsp;the living Messiah whose&nbsp;Spirit renews her youth (Ps. 103:5).</p>

<p>The Coptic Church is rich with evangelistic and ascetic life, genuine patriotic inheritance, heavenly worship, spiritual rituals, effective and living hymns, beautiful icons, etc. The church attracts the heart towards heaven without ignoring actual daily life. We can say that&nbsp;it is&nbsp;an apostolic, contemporary church that carries life and thought to the contemporary man without deviation.&nbsp;</p>

<p>The Church is well known for its numerous saints: ascetics, clergymen and laymen. The church offered many saints throughout ages and is still offering the same today. Leading a sanctified life in communion with God comes first.</p>

